module Calculation
    def add(num1,num2)
        num1 + num2
    end

    def subtract(num1,num2)
        num1 - num2
    end

    def multiply(num1, num2)
        num1 * num2
    end

    def divide(num1, num2)
        num1 / num2
    end

    def moduloDivide(num1, num2)
        num1 % num2
    end
end